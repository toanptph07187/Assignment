    private void btndatphongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndatphongActionPerformed
        // TODO add your handling code here:
        Phong p = pdao.find((String) cbophong.getSelectedItem());
        long millis = System.currentTimeMillis();
        Date date = new Date(millis);
        if (kiemtra()) {
            String cmnd = txtcmnd.getText().trim();
            Khachhang kh = kdao.find(cmnd);
            if (kh != null) {
                int chon = JOptionPane.showConfirmDialog(this, "CMND đã được đăng ký trước đó, Bạn Có muốn cập nhật thông tin khách hàng và tiếp tục đặt phòng"
                        + "\nHọ tên : " + txthoten.getText().trim() + " - Cũ : " + kh.getHoten()
                        + "\nSố điện Thoại : " + txtsodienthoai.getText().trim() + " - Cũ : " + kh.getSdt()
                        + "\nGiới Tính : " + (rdnam.isSelected() ? "Nam" : "Nữ") + " - Cũ :" + (kh.getGtinh() ? "Nam" : "Nữ"));
                if (chon == JOptionPane.YES_OPTION) {
                    Khachhang kh2 = new Khachhang(cmnd, txthoten.getText().trim(), rdnam.isSelected(), txtsodienthoai.getText().trim(), null);
                    if (kdao.update(kh2)) {
                        JOptionPane.showMessageDialog(this, "Cập nhật thông tin khách hàng THÀNH CÔNG");
                        p.setTrangthai(true);

                        if (pdao.update(p)) {
                            JOptionPane.showMessageDialog(this, "Trạng Thái Phòng thay đổi thành công");
                            Cthd cthd = new Cthd(3, kh2, p, date, null, null);
                            if (cdao.save(cthd)) {
                                JOptionPane.showMessageDialog(this, "Đặt Phòng THÀNH CÔNG");
                                cbophong.removeItem(cbophong.getSelectedItem());
                            } else {
                                JOptionPane.showMessageDialog(this, "Đặt phòng THẤT BẠI 2");
                            }
                        } else {
                            JOptionPane.showMessageDialog(this, "Đặt phòng THẤT BẠI 1");
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "Cập nhật thông tin khách hàng THẤT BẠI");
                        return;
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Bạn đã hủy đặt phòng");
                }
            } else {
                int chon = JOptionPane.showConfirmDialog(this, "Xác Nhận Đặt Phòng ?");
                if (chon == JOptionPane.YES_OPTION) {
                    Khachhang kh2 = new Khachhang(cmnd);
                    kh2.setGtinh(rdnam.isSelected());
                    kh2.setHoten(txthoten.getText().trim());
                    kh2.setSdt(txtsodienthoai.getText().trim());
                    if (kdao.save(kh2)) {
                        JOptionPane.showMessageDialog(this, "Thêm khách hàng thành Công");
                        p.setTrangthai(true);
                        pdao.update(p);
                        Cthd cthd = new Cthd(3, kh2, p, date, null, null);
                        if (cdao.save(cthd)) {
                            JOptionPane.showMessageDialog(this, "Đặt Phòng Thành Công");
                             cbophong.removeItem(cbophong.getSelectedItem());
                        } else {
                            JOptionPane.showMessageDialog(this, "Đặt Phòng Thất Bại");
                        }
                    }else{
                        JOptionPane.showMessageDialog(this, "Thêm khách hàng thất bại");
                    }

                }
            }
        }
    }//GEN-LAST:event_btndatphongActionPerformed

