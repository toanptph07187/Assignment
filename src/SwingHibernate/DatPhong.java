    public void loadCombo() {
        cbophong.removeAllItems();
        for (Phong p : pdao.timkiemphongtrong(false)) {
            cbophong.addItem(p.getMaphong());
        }
        loaddetail();
    }

    public void loaddetail() {
        String maphong = (String) cbophong.getSelectedItem();
        Phong p = pdao.find(maphong);
        txtloaiphong.setText(p.getLoaiphong());
        txtgiaphong.setText(String.valueOf(p.getGiaphong()));
        txtsophong.setText(maphong);

    }

    public boolean kiemtra() {
        String name = txthoten.getText().trim();
        if (name.length() == 0) {
            JOptionPane.showMessageDialog(this, "Vui lòng không để trống họ tên");
            return false;
        }
        String cmnd = txtcmnd.getText().trim();
        if (cmnd.length() == 0) {
            JOptionPane.showMessageDialog(this, "Vui lòng không để trống CMND");
            return false;
        }
        if (cmnd.length() > 12 || cmnd.length() < 9) {
            JOptionPane.showMessageDialog(this, "Độ dài CMND phải từ 9 - 12 Số");
            return false;
        }
        try {
            int CMND = Integer.parseInt(cmnd);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "CMND phải là Số");
            return false;
        }
        String ngsinh = txtngaysinh.getText().trim();
        SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");
        df.setLenient(false);
        try {
            java.util.Date d = df.parse(ngsinh);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Hãy nhập ngày sinh hợp lệ ví dụ: 14/04/1997");
            return false;
        }
        String sdt = txtsodienthoai.getText().trim();
        if (sdt.length() == 0) {
            JOptionPane.showMessageDialog(this, "Vui lòng không để trống Số điện thoại");
            return false;
        }
        try {
            int SDT = Integer.parseInt(sdt);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Số Điện Thoại phải là Số");
            return false;
        }
        if (sdt.length() != 10) {
            JOptionPane.showMessageDialog(this, "Số Điện Thoại phải có 10 số");
            return false;
        }
        return true;
    }

